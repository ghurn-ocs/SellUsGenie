// Function to get palette by ID
export const getPaletteById = (id: string): ColorPalette | undefined => {
  return predefinedColorPalettes.find(palette => palette.id === id);
};

// Function to get palettes by category
export const getPalettesByCategory = (category: ColorPalette['category']): ColorPalette[] => {
  return predefinedColorPalettes.filter(palette => palette.category === category);
};

// Function to generate CSS variables from a color palette
export const generateCSSVariables = (palette: ColorPalette): string => {
  return Object.entries(palette.colors).map(([key, value]) => 
    `  --color-${key.replace(/([A-Z])/g, '-$1').toLowerCase()}: ${value};`
  ).join('\n');
};

